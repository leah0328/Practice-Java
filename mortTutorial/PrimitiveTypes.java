public class PrimitiveTypes {

    public static void main(String[] args) {
        byte age = 24;
        long viewsCount = 3_123_456_789L;
        float price = 10.99F;
        char letter = 'A'; // '' for single char
        String fruits = "Apple"; // "" for string or multiple chars
        boolean isEligible = false;


        // byte > short > int > long > float > double

        // byte     : 128
        // short    : 32,768
        // int      : 2,147,483,648
        // long     : 9,223,372,036,854,775,808
        // float    : 6 to 7 decimal digits
        // double   : 15 decimal digits



    }
}

//Primitive types: for storing simply values - no need to allocate memory, ie: boolean,no. character
//Reference types: for storing more objects - need to allocate memory, ie: mail message, date

